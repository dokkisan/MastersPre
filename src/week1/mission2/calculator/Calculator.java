package week1.mission2.calculator;

public class Calculator {

    int plus(int x, int y) {
        return x + y;
    }

    int minus(int x, int y) {
        int result = x - y;
        return result;
    }

    int time(int x, int y) {
        int result = x * y;
        return result;
    }

    int divide(int x, int y) {
        int result = x / y;
        return result;
    }
}
